    @Override
    public void setRotationAngles(float f, float f1, float ticks, float rotationYaw, float rotationPitch, float partialTicks, Entity entity) {
        this.resetToDefaultPose();

        MowzieModelRenderer[] rightArmParts = new MowzieModelRenderer[] { handright, LowerArmRight, UpperArmRight };
        MowzieModelRenderer[] leftArmParts = new MowzieModelRenderer[] { handleft, LowerArmLEFT, UpperArmLEFT };
        MowzieModelRenderer[] tailParts = new MowzieModelRenderer[] { Tail6, Tail5, Tail4, Tail3, Tail2, Tail1 };
        MowzieModelRenderer[] bodyParts = new MowzieModelRenderer[] { Head, Neck1, Neck2, Neck3, Neck4, Neck5, Body3, Body2, Body1 };

        // Walking-dependent animation
        float globalSpeed = 0.45F;
        float globalDegree = 0.5F;
        float height = 1.0F;

        this.bob(Body1, 1F * globalSpeed, height, false, f, f1);
        this.bob(LeftThigh, 1F * globalSpeed, height, false, f, f1);
        this.bob(RightThigh, 1F * globalSpeed, height, false, f, f1);
        LeftThigh.rotationPointY -= -2 * f1 * Math.cos(f * 0.5 * globalSpeed);
        RightThigh.rotationPointY -= 2 * f1 * Math.cos(f * 0.5 * globalSpeed);
        this.chainWave(bodyParts, 1F * globalSpeed, 0.05F, 3, f, f1);
        this.chainWave(tailParts, 1F * globalSpeed, height * 0.05F, 3, f, f1);
        this.chainSwing(tailParts, 0.5F * globalSpeed, height * -0.05F, 2, f, f1);
        this.chainWave(leftArmParts, 1F * globalSpeed, height * 0.05F, 3, f, f1);
        this.chainWave(rightArmParts, 1F * globalSpeed, height * 0.05F, 3, f, f1);

        this.walk(Head, 1F * globalSpeed, 0.1F, true, 0F, -0.2F, f, f1);
        this.walk(Neck1, 1F * globalSpeed, 0.02F, false, 0F, 0.04F, f, f1);
        this.walk(Neck2, 1F * globalSpeed, 0.02F, false, 0F, 0.04F, f, f1);
        this.walk(Neck3, 1F * globalSpeed, 0.02F, false, 0F, 0.04F, f, f1);
        this.walk(Neck4, 1F * globalSpeed, 0.02F, false, 0F, 0.04F, f, f1);
        this.walk(Neck5, 1F * globalSpeed, 0.02F, false, 0F, 0.04F, f, f1);

        this.walk(LeftThigh, 0.5F * globalSpeed, 0.8F * globalDegree, false, 0F, 0.2F, f, f1);
        this.walk(LeftCalf1, 0.5F * globalSpeed, 1F * globalDegree, true, 1F, 0.4F, f, f1);
        this.walk(LeftCalf2, 0.5F * globalSpeed, 1F * globalDegree, false, 0F, 0F, f, f1);
        this.walk(FootLeft, 0.5F * globalSpeed, 1.5F * globalDegree, true, 0.5F, 0.1F, f, f1);

        this.walk(RightThigh, 0.5F * globalSpeed, 0.8F * globalDegree, true, 0F, 0.2F, f, f1);
        this.walk(RightCalf1, 0.5F * globalSpeed, 1F * globalDegree, false, 1F, 0.4F, f, f1);
        this.walk(RightCalf2, 0.5F * globalSpeed, 1F * globalDegree, true, 0F, 0F, f, f1);
        this.walk(FootRight, 0.5F * globalSpeed, 1.5F * globalDegree, false, 0.5F, 0.1F, f, f1);

        // idling
        this.chainWave(tailParts, 0.1F, 0.05F, 2, ticks, 1F);
        this.chainWave(bodyParts, 0.1F, -0.03F, 4, ticks, 1F);
        this.chainWave(rightArmParts, 0.1F, -0.1F, 4, ticks, 1F);
        this.chainWave(leftArmParts, 0.1F, -0.1F, 4, ticks, 1F);

        this.faceTarget(rotationYaw, rotationPitch, 1.0F, Head, Neck1, Neck5);
        NeckUnder2.rotationPointX -= rotationYaw * 0.025;

        ((EntitySpinosaurus) entity).tailBuffer.applyChainSwingBuffer(tailParts);
    }

